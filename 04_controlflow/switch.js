const month = "march"

switch (month) {
    case "january":
        console.log("This month is January");
        break;
    case "february":
        console.log("This month is February");
        break;
    case "march":
        console.log("This month is March");
        break;
    case "april":
        console.log("This month is April");
        break;

    default:
        console.log("Default case match");
        break;
}